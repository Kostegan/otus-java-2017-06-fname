package ru.otus.l2;

/**
 *
 */
public class MyClass {
    private int number;
    private String str;

    public MyClass() {
    }

    public MyClass(int number, String str) {
        this.number = number;
        this.str = str;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }
}
